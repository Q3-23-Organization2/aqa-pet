package homework5;

public class ChangeEvenToZero {
    public static void main(String[] args) {
        int[] numbers = {23, 4, 12, 58, 93, 97, 113, 43};

        for (int i = 0; i < numbers.length; ++i) {
            if (numbers[i] % 2 == 0) {
                System.out.println(i = 0);
            }
            else {
                System.out.println(numbers[i]);
            }
        }

    }
}
